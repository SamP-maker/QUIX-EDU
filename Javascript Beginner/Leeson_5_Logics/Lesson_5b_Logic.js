// Example 1: Count from 1 to 5
for (let i = 1; i <= 5; i++) {
    // i is 1, 2, 3, 4, 5
}

// Example 2: Count backwards from 5 to 1
for (let i = 5; i >= 1; i--) {
    // i is 5, 4, 3, 2, 1
}

// Example 3: Print even numbers from 2 to 10
for (let i = 2; i <= 10; i += 2) {
    // i is 2, 4, 6, 8, 10
}

